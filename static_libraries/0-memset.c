#include "main.h"

/**
* *_memset - function
* @s: character string pointer
* @b: character
* @n: unsigned integer
* Description: fill memory with a constant byte
*
* Return: pointer to memory area s
*/
char *_memset(char *s, char b, unsigned int n)
{
unsigned int i;
for (i = 0; i < n; i++)
{
s[i] = b;
}
return (s);
}
